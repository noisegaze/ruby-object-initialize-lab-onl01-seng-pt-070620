class Dog 
  def initialize(name,breed = "Mutt")
    @name = name 
    @breed = breed 
  end 
  
  def name=(name)
    @name 
  end 
  
  def name 
    @name 
  end 
  
  def breed=(breed) 
   if @breed 
     @breed 
   else
     "Mutt"
   end
  end 
  
  def breed  
   if @breed 
     @breed 
   else
     "Mutt"
   end
  end 
end 


